JVector2::JVector2(const JPoint2& p) : mData(p.mData) {}
JVector2::JVector2(const JPoint3& p) {
	mData.x = p.mData.x; mData.y = p.mData.y;
}
JVector2::JVector2(const JVector3& v){
	mData.x = v.mData.x; mData.y = v.mData.y;
}
/////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////// JVector3 //////////////////////////////////////////
