JVector3::JVector3(const JPoint3& p) : mData(p.mData)  {}
JVector3::JVector3(const JNormal3& n) : mData(n.mData) {}
/////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////// JPoint2 ///////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////
JPoint2::JPoint2(const JPoint3& p)
{
	mData.x = p.mData.x;
	mData.y = p.mData.y;
}
